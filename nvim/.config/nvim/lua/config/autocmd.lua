local function augroup(name)
    return vim.api.nvim_create_augroup('nvim' .. name, { clear = true })
end

-- Highlight when yanking (copying) text
--  Try it with `yap` in normal mode
--  See `:help vim.hl.on_yank()`
vim.api.nvim_create_autocmd('TextYankPost', {
    desc = 'Highlight when yanking (copying) text',
    group = augroup 'kickstart-highlight-yank',
    callback = function()
        vim.hl.on_yank()
    end,
})

-- Python overwrite ]] and [[ mapping
vim.api.nvim_create_autocmd('FileType', {
    pattern = 'python',
    callback = function()
        vim.keymap.del('n', ']]', { buffer = true })
        vim.keymap.del('n', '[[', { buffer = true })

        vim.keymap.set('n', ']]', function()
            Snacks.words.jump(vim.v.count1)
        end, { buffer = true, desc = 'Next reference' })

        vim.keymap.set('n', '[[', function()
            Snacks.words.jump(-vim.v.count1)
        end, { buffer = true, desc = 'Previous reference' })
    end,
})

-- Strip trailing spaces before write
vim.api.nvim_create_autocmd({ 'BufWritePre' }, {
    group = augroup 'strip_space',
    pattern = { '*' },
    callback = function()
        vim.cmd [[ %s/\s\+$//e ]]
    end,
})

-- Check if we need to reload the file when it changed
vim.api.nvim_create_autocmd({ 'FocusGained', 'TermClose', 'TermLeave' }, {
    group = augroup 'checktime',
    command = 'checktime',
})

-- resize splits if window got resized
vim.api.nvim_create_autocmd({ 'VimResized' }, {
    group = augroup 'resize_splits',
    callback = function()
        vim.cmd 'tabdo wincmd ='
    end,
})

-- go to last loc when opening a buffer
vim.api.nvim_create_autocmd('BufReadPost', {
    group = augroup 'last_loc',
    callback = function()
        local mark = vim.api.nvim_buf_get_mark(0, '"')
        local lcount = vim.api.nvim_buf_line_count(0)
        if mark[1] > 0 and mark[1] <= lcount then
            pcall(vim.api.nvim_win_set_cursor, 0, mark)
        end
    end,
})

-- wrap and check for spell in text filetypes
vim.api.nvim_create_autocmd('FileType', {
    group = augroup 'wrap_spell',
    pattern = { 'gitcommit', 'markdown' },
    callback = function()
        vim.opt_local.wrap = true
        vim.opt_local.spell = true
    end,
})

-- Auto create dir when saving a file, in case some intermediate directory does not exist
vim.api.nvim_create_autocmd({ 'BufWritePre' }, {
    group = augroup 'auto_create_dir',
    callback = function(event)
        if vim.bo[event.buf].buftype ~= '' then
            return
        end

        if event.match:match '^oil://' then
            return
        end

        local file = vim.loop.fs_realpath(event.match) or event.match
        vim.fn.mkdir(vim.fn.fnamemodify(file, ':p:h'), 'p')
    end,
})

-- Macro recording notifications
vim.api.nvim_create_autocmd('RecordingEnter', {
    callback = function()
        vim.notify('Macro recording started', vim.log.levels.INFO, {
            title = 'Macro',
            timeout = 2000,
        })
        -- Print to command line for debugging
        vim.cmd "echo 'Recording macro...'"
    end,
})

vim.api.nvim_create_autocmd('RecordingLeave', {
    callback = function()
        vim.notify('Macro recording stopped', vim.log.levels.INFO, {
            title = 'Macro',
            timeout = 2000,
        })
        -- Print to command line for debugging
        vim.cmd "echo 'Macro recording stopped'"
    end,
})

-- Function to show macro replay notification
local function show_macro_replay(count)
    local message = count > 1
            and string.format('Replaying macro %d times', count)
        or 'Replaying macro'
    vim.notify(message, vim.log.levels.INFO, {
        title = 'Macro',
        timeout = 2000,
    })
end
